/* timer interrupt callback, hit when timer expires */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  /* if pin is low, and button has been pressed(state == 1), turn the led on
  reset the state, and stop the timer */
  if((HAL_GPIO_ReadPin(myPushButton_GPIO_Port, myPushButton_Pin) == GPIO_PIN_RESET) &&
     (state == 1))//if gpio line is low and state is 1
  {
    HAL_GPIO_TogglePin(GPIOG, GPIO_PIN_13);//toggle green LED
    HAL_GPIO_WritePin(GPIOD, measureOutput_Pin, GPIO_PIN_RESET);//state pin reset, visual indicatior for saleae
    state = 0;//state pin reset
    HAL_TIM_Base_Stop_IT(&htim2);//stop timer
  }

  return;
}
/* button interrupt callback */
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
  /* if button pressed is my button, and button has not been pressed*/
	if((GPIO_Pin == myPushButton_Pin) && (state == 0))
	{
    state = 1;//change state to button is pressed
    HAL_GPIO_WritePin(GPIOD, measureOutput_Pin, GPIO_PIN_SET);//state pin on, visual indicator for saleae
    HAL_TIM_Base_Start_IT(&htim2);//start timer
	}

  return;
}

